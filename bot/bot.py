    """–û–±—Ä–∞–±–æ—Ç—á–∏–∫ –ø–æ–ª—É—á–µ–Ω–∏—è –¥–æ–∫—É–º–µ–Ω—Ç–∞, —Ñ–æ—Ç–æ –∏–ª–∏ –∞—É–¥–∏–æ"""

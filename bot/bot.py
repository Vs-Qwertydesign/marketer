    # –§–æ—Ä–º–∏—Ä—É–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ —Å–æ —Å–ø–∏—Å–∫–æ–º –ø—Ä–æ–µ–∫—Ç–æ–≤
    projects_text = "üìÅ –í–∞—à–∏ –ø—Ä–æ–µ–∫—Ç—ã:\n\n"
    for i, project in enumerate(projects, 1):
        creation_date = project.created_at.strftime("%d.%m.%Y")
        projects_text += f"{i}. {project.name} (—Å–æ–∑–¥–∞–Ω {creation_date})\n"
        projects_text += f"   –°—Ç–∞—Ç—É—Å: {project.status}\n"
        if project.description:
            projects_text += f"   –û–ø–∏—Å–∞–Ω–∏–µ: {project.description[:50]}{'...' if len(project.description) > 50 else ''}\n"
        projects_text += "\n"
    
    await message.answer(projects_text)

